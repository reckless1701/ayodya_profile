                <p>
                Sebuah musik mengalun, saat enam penari itu masuk ruangan. Beberapa tamu undangan langsung mengikuti 
                irama khas Papua itu. Ya, Ke enam penari itu membawakan Tari Mambo Simbo saat peluncuran kanal budaya, 
                Kulturina.id yang digelar di Hotel J.S Luwansa Kuningan, Jakarta Selatan, Jumat (29/11/19).
                </p>

                <p>
                Tari Mambo Simbo adalah tarian yang menceritakan tentang seorang anak laki-laki yang lari dari 
                desanya. Penduduk desa dan orang tua Mambo menyusuri sungai dan hutan untuk mencari Mambo. 
                Sayangnya, Ibu Mambo meninggal di tengah pencarian tersebut. Penduduk desa menangisi kepergiannya. 
                Setelah itu, penduduk desa kembali melanjutkan pencarian. Akhirnya mereka menemukan Mambo dan mereka
                 merayakan keberhasilan ini. Itulah cerita yang melatarbelakangi terciptanya tarian Mambo Simbo ini.
                </p>

                <p>
                Pada acara tersebut, sebagian tamu undangan juga ada yang maju ke depan panggung ikut serta 
                menari Mambo Simbo sambil berlanggak-lenggok mengikuti irama musik. Dengan adanya penampilan 
                anak-anak Papua ini tamu undangan terlihat sangat terhibur.
                </p>

                <p>
                Selain alunan musik bernuansa penuh kegembiraan, ternyata tarian Mambo Simbo ini memiliki 
                filosofi yang sangat mendalam yang menggambarkan kehidupan orang-orang Papua.
                </p>

                <p>
                Vellicia Mugo, salah satu penari yang juga menggawangi Mambri Collection Papua mengungkapkan, 
                tarian Mambo Simbo yang mereka tampilkan tersebut memilki simbol kegembiraan, tali persaudaraan, 
                keakrabaan dan kekeluargaan yang erat.
                </p>
                
                <blockquote>
                  <i class="icofont-quote-left quote-left"></i>
                  <p>
                  Tarian ini adalah simbol kegembiraan, persaudaraan, untuk merekatkan tali kekeluargaan
                  </p>
                  <i class="las la-quote-right quote-right"></i>
                  <i class="icofont-quote-right quote-right"></i>
                </blockquote>

                <p>
                Kemudian, kostum yang dikenakan para penari ini juga sangat unik karena berasal dari 
                bahan-bahan yang diambil dari alam Papua seperti dibuat dari serat-serat pohon, rumputan dan 
                daun-daun.Vellicia melanjutkan, kostum ini merupakan pakaian adat Papua yang terbuat dari 
                susunan daun sagu kering dan digunakan untuk menutupi sebagian tubuh bawah.
                </p>

                <p>
                “Bahan-bahan yang digunakan berasal dari daun sagu, rumbai-rumbai ini dari daun sagu kering,” tutur Vellicia.
                </p>

                <img src="assets/blog-single/tari-mambo-simbo/mambo-simbo-2.jpg" class="img-fluid" alt="">

                <p>
                Rikard Bagun, Jurnalis Senior yang juga dewan pengarah Badan Pembinaan Ideologi Pancasila (BPIP) 
                memberikan pandangan yang menarik tentang pandangan kebudyaan Papua ini. Ia menganggap bahwa 
                acara kebudayaan seperti ini perlu terus digalakan.
                </p>

                <p>
                “Narasinya bukan lagi politik, namun ini wacana kebudayaan yag sangat menarik, kebudayaan 
                Papua adalah simbol kekuatan papua yang genuin, dan kita bisa mendapatkan perspektif yang 
                lain,” pungkasnya.
                </p>

                <img src="assets/blog-single/tari-mambo-simbo/mambo-simbo-3.jpg" class="img-fluid" alt="">

                <p>
                Selain itu, tarian Mambo Simbo ini sudah mulai dikenal luas di masyarakat dan tidak hanya 
                ditampilkan di dalam negeri saja tetapi pernah ditampilkan dalam acara-acara panggung 
                internasional, diantaranya acara yang menampilkan Revolution 2018 yang digelar di Marina 
                Bay Sands Expo & Convention Singapura pada 23-25 Maret 2018 dan ditampilkan pada kesempatan 
                Resepsi Diplomatik HUT RI ke-74 di Hotel JW Marriott Ankara, 21 September 2019.
                </p>

                <p>
                Acara peluncuran kanal Kulturina.id ini adalah sebagai jembatan untuk mengembalikan wacana 
                kebudayaan dan warisan budaya leluhur yang ada di Indonesia demi memperkuat rasa nasionalisme 
                yang diawali dengan rasa persaudaraan yang kuat.
                </p>

